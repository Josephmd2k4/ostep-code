#include <stdio.h>

int main(){
    //Create Variables
    int myNum = 15; //int is a data type for integers
    float myFloatNum = 5.99; //float is a data type for floating point numbers
    char myLetter = 'D'; //char is a data type for characters

    //Print Variables
    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
}